#Escribe un programa que compare dos números y muestre si son iguales o no utilizando operadores de comparación (por ejemplo, ==, !=)

num1 = int(input("primer numero: "))
num2 = int(input("segundo numero: "))

if num1 == num2:
    print("son iguales")
elif  num1 != num2:
    print("son diferentes")
